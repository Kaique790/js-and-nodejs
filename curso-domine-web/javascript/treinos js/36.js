function contas(x, y) {
    soma = x + y
    sub = x - y
    mult = x * y
    div = x / y

    
    console.log(`soma: ${x} + ${y} = ${soma}`)
    console.log(`subtração: ${x} - ${y} = ${sub}`)
    console.log(`multiplicação: ${x} x ${y} = ${mult}`)
    console.log(`divisão: ${x} / ${y} = ${div}`)
}

contas(10, 20)